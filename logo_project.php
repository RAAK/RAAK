                <h3 class="din-schrift blue_20">About the project</h3>
                <hr />
                <p>
This is not in the typical crowdsourcing project; oh no, that&#8217;s soooooooooo 2009. In this project you don&#8217;t design the whole logo. And the creation process won&#8217;t have a cut-off date. We really want to make this a co-creation experiment.
                </p>
                <p>
We need our brand to reflect what we&#8217;re about: <strong>open, social, digital, participatory, creative and constantly changing</strong>. This unique branding exercise is about process as well as result.
                </p>
                <p>
So we would love you to design 1 gorgeous, beautiful letter: an R, an A or a K. In any style you want; just show off your skills.
                </p>
                <p>
When you submit your creation, it will become part of our animated logo loop. The R&#8217;s, A&#8217;s and K&#8217;s will rotate in a random way to create hundreds of thousands, millions of logos. Each with a unique, unexpected, beautiful, clashing combination of letters.
                </p>
                <p>
We call it PDLB: Process-Driven Liquid Branding.<br />
Or JPF: Just Plain Fun.
                </p>
                <p>
Each uploaded letter (considering it ticks the technical boxes mentioned below) will get a spot on our <a href="http://wewillraakyou.com/logo-project/logo-archive">letter archive page</a>, where you can <em>include a link to your portfolio</em>. And you&#8217;re welcome, no encouraged even, to use your letter elsewhere.
                </p>
